// 1) იპოვეთ და გაასწორეთ შეცდომა შემდეგ პროგრამულ ფრაგმენტში.
let i = 1;
let sum = 0;
while (i < 10) {
    if (i % 2 != 0) {
        sum = sum + i;
    }

    i++; //ეს აკლდა, იციკლებოდა while
}
console.log(sum);


// 2) კონსოლში დავბეჭდოთ 1დან 50მდე 3ის ჯერადი რიცხვების ჯამი, ციკლის გამოყენებით.

let j = 1;
let total = 0;

while (j < 50) {
    if (j % 3 === 0)
        total += j;
    j++;
}
